    /**
     * 根据分组key，取默认的apiId
     */
    public static function keyDefaultApiId($groupKey) {
        $con[] = ['group_key', '=', $groupKey];
        $info = self::find($con, 86400);
        return Arrays::value($info, 'default_api_id');
    }

